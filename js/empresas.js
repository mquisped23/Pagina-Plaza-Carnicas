function updateScrollIndicator() {
  const scrollContainer = document.getElementById("partners-scroll-track");
  const progressLine = document.getElementById("carousel-progress-line");
  
  if (!scrollContainer || !progressLine) return;

  const maxScroll = scrollContainer.scrollWidth - scrollContainer.clientWidth;
  
  if (maxScroll <= 0) {
    progressLine.style.transform = "scaleX(1)";
    return;
  }

  const scrollPercent = scrollContainer.scrollLeft / maxScroll;
  // Usar transform para un rendimiento CSS superior y sin lag
  progressLine.style.transform = `scaleX(${Math.min(Math.max(scrollPercent, 0), 1)})`;
}
